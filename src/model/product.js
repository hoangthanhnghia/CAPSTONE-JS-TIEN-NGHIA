function Product(name, price, screen, blackCamera, frontCamera, img, desc, type){
    this.productName = name;
    this.productPrice = price;
    this.productScreen = screen;
    this.productBlackCamera = blackCamera;
    this.productFrontCamera = frontCamera;
    this.productImg = img;
    this.productDesc = desc;
    this.productType = type;
}
